#include<stdio.h>

int main(void)
{
	int a, b;
	
	for (a = 1; a < 10; a++)
	{
		printf("________________________________________________________________________________\n");
		for (b = 1; b < 10; b++)
		{
			printf("|%d*%d=%d, ", a, b, a*b);
			if (a*b <10)
			{
				printf(" ");
			}
		}
		printf("|\n");
	}
	printf("________________________________________________________________________________\n");
	
	return 0;
}
